package utility;

/**
 * Enum to define types of charity events.
 *
 * FUNDRAISER: Represents events aimed at raising funds for a cause. AWARENESS:
 * Represents events focused on raising awareness about a particular issue.
 * VOLUNTEER_DRIVE: Represents events that aim to recruit volunteers for a
 * cause. DONATION_DRIVE: Represents events organized to collect donations, such
 * as goods or money. /**
 *
 * @author Terence Goh Poh Xian
 */
public enum EventType {
    FUNDRAISER,
    AWARENESS,
    VOLUNTEER_DRIVE,
    DONATION_DRIVE
}
